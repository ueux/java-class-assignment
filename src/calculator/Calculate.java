    private void num1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_num1ActionPerformed
        // TODO add your handling code here:
        str += "1";
        t1.setText(str);
        if (nflag == pflag) {
            str1 += "1";
        } else {
            str2 += "1";
        }
        if (str2.equals("")) {
            str2 = "0";
        }
        switch (op) {
            case 1 ->
                ans = String.valueOf(Double.parseDouble(str1) + Double.parseDouble(str2));
            case 2 ->
                ans = String.valueOf(Double.parseDouble(str1) - Double.parseDouble(str2));
            case 3 ->
                ans = String.valueOf(Double.parseDouble(str1) * Double.parseDouble(str2));
            case 4 ->
                ans = String.valueOf(Double.parseDouble(str1) / Double.parseDouble(str2));
            default -> {
                ans = str1;
            }
        }

        t1.setText(str);
        DecimalFormat df = new DecimalFormat("#.######");
        t2.setText(df.format(Double.parseDouble(ans)));
    }//GEN-LAST:event_num1ActionPerformed

