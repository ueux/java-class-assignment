    private void op6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_op6ActionPerformed
        // TODO add your handling code here:
        if (str.endsWith("+") || str.endsWith("-") || str.endsWith("*") || str.endsWith("/")) {
            str = str.substring(0, str.length() - 1);
            if (!(str.contains("+") || str.contains("-") || str.contains("*") || str.contains("/"))) {
                str1 = str;
                str2 = "0";
                nflag = pflag = 0;
                op = 0;
                t1.setText(str);
                System.out.println(str1 + " " + str2);
                System.out.println(pflag + " " + nflag);
            } else {
                int maxi = Math.max(Math.max(str.lastIndexOf("+"), str.lastIndexOf("-")), Math.max(str.lastIndexOf("*"), str.lastIndexOf("/")));
                str2 = str.substring(maxi + 1);
                switch (str.charAt(maxi)) {
                    case '+' ->
                        op = 1;
                    case '-' ->
                        op = 2;
                    case '*' ->
                        op = 3;
                    case '/' ->
                        op = 4;
                    default -> {
                        op = 0;
                    }
                }

                switch (op) {
                    case 1 ->
                        str1 = String.valueOf(Double.parseDouble(ans) - Double.parseDouble(str2));
                    case 2 ->
                        str1 = String.valueOf(Double.parseDouble(ans) + Double.parseDouble(str2));
                    case 3 ->
                        str1 = String.valueOf(Double.parseDouble(ans) / Double.parseDouble(str2));
                    case 4 ->
                        str1 = String.valueOf(Double.parseDouble(ans) * Double.parseDouble(str2));
                    default -> {
                        ans = str;
                    }
                }
                pflag = nflag;
                nflag = (nflag + 1) % 2;
                t1.setText(str);
                System.out.println(str1 + " " + str2);
                System.out.println(pflag + " " + nflag);
            }
        } else {
            if (str.length() > 0) {
                str = str.substring(0, str.length() - 1);
            }
            if (nflag == pflag && str1.length() > 0) {
                str1 = str1.substring(0, str1.length() - 1);
            } else if (str2.length() > 0) {
                str2 = str2.substring(0, str2.length() - 1);
            }
            if (str1.equals("")) {
                str1 = "0";
            }
            if (str2.equals("")) {
                if (op == 3 || op == 4) {
                    str2 = "1";
                } else {
                    str2 = "0";
                }
            }
            switch (op) {
                case 1 ->
                    ans = String.valueOf(Double.parseDouble(str1) + Double.parseDouble(str2));
                case 2 ->
                    ans = String.valueOf(Double.parseDouble(str1) - Double.parseDouble(str2));
                case 3 ->
                    ans = String.valueOf(Double.parseDouble(str1) * Double.parseDouble(str2));
                case 4 ->
                    ans = String.valueOf(Double.parseDouble(str1) / Double.parseDouble(str2));
                default -> {
                    ans = str1;
                }
            }
            t1.setText(str);
            DecimalFormat df = new DecimalFormat("#.######");
            t2.setText(df.format(Double.parseDouble(ans)));
            System.out.println(str1 + " " + str2);
            System.out.println(pflag + " " + nflag);
        }
    }//GEN-LAST:event_op6ActionPerformed

